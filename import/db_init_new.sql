USE mirrors;
DROP TABLE IF EXISTS log;

CREATE TABLE log (
	ipv4 BIGINT(10),
	ipv4_0 SMALLINT(3),
	ipv4_1 SMALLINT(3),
	ipv4_2 SMALLINT(3),
	ipv4_3 SMALLINT(3),
	ipv6_0 BIGINT(10),
	ipv6_1 BIGINT(10),
	ipv6_2 BIGINT(10),
	ipv6_3 BIGINT(10),
	time INT(10) NOT NULL,
	year SMALLINT(4) NOT NULL,
	month TINYINT(2) NOT NULL,
	yearday SMALLINT(3) NOT NULL,
	monthday SMALLINT(2) NOT NULL,
	weekday TINYINT(1) NOT NULL,
	daymin SMALLINT(4) NOT NULL,
	daytime MEDIUMINT(5) NOT NULL,
	hour TINYINT(2) NOT NULL,
	status SMALLINT(3) NOT NULL,
	length BIGINT(20) NOT NULL,
	url VARCHAR(255),
	url_0 VARCHAR(255),
	url_1 VARCHAR(255),
	url_2 VARCHAR(255),
	url_3 VARCHAR(255),
	url_4 VARCHAR(255),
	url_5 VARCHAR(255),
	url_6 VARCHAR(255),
	url_7 VARCHAR(255),
	url_8 VARCHAR(255),
	url_9 VARCHAR(255),
	filename VARCHAR(255),
	filename_noext VARCHAR(255),
	extension VARCHAR(7),
	referer VARCHAR(255),
	ua_0 VARCHAR(31),
	ua VARCHAR(255)
) ENGINE=BRIGHTHOUSE;
